let rec reverse seznam =
let rec reverse1 seznam acc = 
  match seznam with
  | [] -> acc
  | prvi :: ostalo -> reverse1 ostalo ( prvi :: acc ) in
reverse1 seznam []

(*----------------------------------------------------------------------------*]
